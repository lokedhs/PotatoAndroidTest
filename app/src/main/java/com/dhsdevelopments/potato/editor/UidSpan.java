package com.dhsdevelopments.potato.editor;

public class UidSpan
{
    private String uid;
    private String name;

    public UidSpan( String uid, String name ) {
        this.uid = uid;
        this.name = name;
    }

    public String getUid() {
        return uid;
    }

    public String getName() {
        return name;
    }
}
